## 일반적으로 피연산자는 True 또는 False 값을 가지는 연산

a = 20
print(not a < 20)

# AND 판별
# T and T = T
# T and F = F
# F and T = F
# F and F = F
print(a<30 and a != 30)


# OR 판별
# T or T = T
# T or F = T
# F or T = T
# F or F = F

def f():
    print('Hello World')



a < 10 or f()


